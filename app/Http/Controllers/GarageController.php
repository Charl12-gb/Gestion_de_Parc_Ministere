     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $garage = new Garage();
        $garage->nomgarage = $request->nomgarage;
        $status = $garage->save();

        if( $status ) $parametre = ['status'=>true, 'msg'=>'Garage Enrégistré avec succès'];
        else $parametre = ['status'=>false, 'msg'=>'Erreur lors de l\'enregistrement'];
        return redirect()->route('garages')->with($parametre);
    }

